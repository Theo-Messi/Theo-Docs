/* ?? 运算符 */
const a = 0 ?? 'maomao'         // 0
const b = '' ?? 'maomao'        // ''
const c = null ?? 'maomao'      // 'maomao'
const d = undefined ?? 'maomao' // 'maomao'

/* || 运算符 */
const A = 0 || 'maomao'         // 'maomao'
const B = '' || 'maomao'        // 'maomao'
const C = null || 'maomao'      // 'maomao'
const D = undefined || 'maomao' // 'maomao'
